# This program shows Python concepts


# Store the first number in a variable
number_one = 100

# Store the second number in a variable
number_two = 2

# BASIC ARITHMETIC OPERATIONS

# Add the two numbers
addition_result = number_one + number_two

# Subtract the second number from the first
subtraction_result = number_one - number_two

# Multiply the two numbers
multiplication_result = number_one * number_two

# Divide the first number by the second
division_result = number_one / number_two

# Print a title for the output
print("=== TASK 3: BASIC PYTHON ===")

# Print the values of the variables
print("Number One:", number_one)
print("Number Two:", number_two)

# Print the results with clear labels
print("Addition Result:", addition_result)
print("Subtraction Result:", subtraction_result)
print("Multiplication Result:", multiplication_result)
print("Division Result:", division_result)

